 private void carregarVendas() {
        try {
            VendasDAO dao = new VendasDAO();
            List<LgsVendas> listaVendases = (List<LgsVendas>) dao.listAll();
            atualizarContador(listaVendases.size());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
   
    private void atualizarContador(int total) {
        if (total == 0) {
            lblTotalUsuarios.setText("Nenhum usu치rio cadastrado");
        } else if (total == 1) {
            lblTotalUsuarios.setText("1 usu치rio cadastrado");
        } else {
            lblTotalUsuarios.setText(total + " usu치rios cadastrados");
        }
    }
